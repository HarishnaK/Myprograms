class Calculator {
    static add(a, b) {
      return a + b;
    }
  
    static subtract(a, b) {
      return a - b;
    }
  }
  
  console.log(Calculator.add(5, 3)); 
  console.log(Calculator.subtract(8, 3)); 
  console.log(Calculator.add(10,4));
  console.log(Calculator.subtract(8, 34));
